// - O QUE SÃO VARIÁVEIS?
var var1 = 1;
let var2 = 2;
// var e let são formas de declarar variáveis
// suas estruturas são:
// var ou let -> palavras chave
// var1 ou var2 -> nome da variável
// 1 ou 2 -> valor da variável
//  - TRUQUES QUE O JAVASCRIPT TEM :)
var1 = '1';
var2 = '2';
var1 = true;
var2 = false;
//  ** Variável significa que o valor pode mudar


// - O QUE SÃO CONSTANTES?
const const1 = 3;
const const2 = 4;
// const é uma forma de declarar constantes
// const -> palavras chave
// const1 ou const2 -> nome da constante
// 3 ou 4 -> valor da constante
// const1 = '2'; ** CONTANTES NÃO MUDAM!!!


// - COMO IMPRIMIR NO TERMINAL?
console.log(var1);
console.log(var2);
console.log(const1);
console.log(const2);